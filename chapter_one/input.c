#include <stdio.h>

int main () {
    int a, b;
    float c, d;
    char cs;


    scanf ("%d", &a);
    scanf ("%d", &b);
    scanf ("%f", &c);
    scanf ("%f", &d);
    scanf ("%c", &cs);

    printf ("%d %d\n", a, b);
    printf ("%f %f\n", c, d);
    printf ("%c\n", cs);

    return 0;
}

