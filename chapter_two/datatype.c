#include <stdio.h>

//datatype -> basic datatype in c-programming language
int main() {
    int num = 10;
    float num2 = 1.0f;
    char ch = 'S';

    num = num2;

    printf("%d\n", num);
    printf("%f\n", num2);
    printf("%c\n", ch);

    return 0;
}

/**
 * c datatype -> 3 datatype in c programming language
 * integer datatype -> short, int, long, long long,
 * float datatype -> float, double, long double, 
 * char datatype -> char, 
 */