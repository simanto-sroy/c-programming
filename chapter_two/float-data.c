#include <stdio.h>

int main (void) {

    //Float DataType - %f
    float f = 123.456F; 
    printf("Float DataType - %f\n", f);

    //Float DataType - %lf
    double d = 145623.456; 
    printf("Double DataType - %lf\n", d);


    //Float DataType - %Lf
    long double ld = 15684289474323.4880; 
    printf("Long Double DataType - %Lf\n", ld);

    return 0;
}